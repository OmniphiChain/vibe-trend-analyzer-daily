export const featuredPlugins = mockPlugins.filter(plugin => plugin.featured);

export const getPluginsByCategory = (category: PluginCategory): Plugin[] => {
  if (category === 'all') return mockPlugins;
  return mockPlugins.filter(plugin => plugin.category === category);
};

export const getPopularPlugins = (limit: number = 6): Plugin[] => {
  return [...mockPlugins]
    .sort((a, b) => b.downloadCount - a.downloadCount)
    .slice(0, limit);
};

export const getNewestPlugins = (limit: number = 6): Plugin[] => {
  return [...mockPlugins]
    .sort((a, b) => new Date(b.dateCreated).getTime() - new Date(a.dateCreated).getTime())
    .slice(0, limit);
};
